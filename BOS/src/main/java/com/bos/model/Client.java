package com.bos.model;

public class Client
{
    private String clientCountry;
    private String clientName;
    private String clientHp;
    private String clientBankName;
    private String clientBankNumber;
    private String clientAddress;
    private String clientDistricts;
    private String clientCity;

    public String getClientCountry()
    {
        return clientCountry;
    }

    public void setClientCountry(String clientCountry)
    {
        this.clientCountry = clientCountry;
    }

    public String getClientName()
    {
        return clientName;
    }

    public void setClientName(String clientName)
    {
        this.clientName = clientName;
    }

    public String getClientHp()
    {
        return clientHp;
    }

    public void setClientHp(String clientHp)
    {
        this.clientHp = clientHp;
    }

    public String getClientBankName()
    {
        return clientBankName;
    }

    public void setClientBankName(String clientBankName)
    {
        this.clientBankName = clientBankName;
    }

    public String getClientBankNumber()
    {
        return clientBankNumber;
    }

    public void setClientBankNumber(String clientBankNumber)
    {
        this.clientBankNumber = clientBankNumber;
    }

    public String getClientAddress()
    {
        return clientAddress;
    }

    public void setClientAddress(String clientAddress)
    {
        this.clientAddress = clientAddress;
    }

    public String getClientDistricts()
    {
        return clientDistricts;
    }

    public void setClientDistricts(String clientDistricts)
    {
        this.clientDistricts = clientDistricts;
    }

    public String getClientCity()
    {
        return clientCity;
    }

    public void setClientCity(String clientCity)
    {
        this.clientCity = clientCity;
    }
}